fruits = {"orange": 50, "apple": 100, "banana": 200}
for i in range(5):
    fruit = input("This is fruit store. What do you want?:")
    if fruit in fruits:
        print(f"we have {fruit}. {fruits[fruit]} left!")
    else:
        print(f"Sorry, we don't have {fruit}")

